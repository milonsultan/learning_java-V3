public class ArithmeticOperators {
    public static void main(String[] args) {

        int num1, num2, result;

        num1 = 10;
        num2 =20;
        result = num1 + num2;
        System.out.println("The Result is: " + result);

        int square = num1*num2;
        System.out.println("The Square of the two numbers is " + square);

        int num4 = 10, num5 = 20;
        num2 = num4 + num5;
        System.out.println("The sum is: " + num2);

        int num6 = 10;
        int num7 = 20;
        int result6 = num6 + num7;
        int result10 = num6 - num7;
        int result11 = num7 / num6;
        System.out.println("The Result is: " + result6);
        System.out.println("The Result is: " + result10);
        System.out.println("The Result is: " + result11);
    }

}